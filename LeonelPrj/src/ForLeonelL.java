public class ForLeonelL {
    public static void ForL(int size) {
        System.out.println("For Letra L :");
        for (int i = 0; i < size; i++) {
            if (i == size -2 ) {
                for (int j = 1; j < size; j++) {
                    System.out.print("*");
                }
            } else {
                System.out.println("*");
            }
        }
    }
}

 
